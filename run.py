# ======================= 改进的区间输入函数 =======================
def 区间输入(左: int, 右: int, 提示: str) -> str:
    """
    显示格式：[左 _   右]   （左后有一个空格，然后一个下划线，再三个空格）
    光标落在第一个下划线处。
    输入后重新格式化为：[左 _输入_ 右]
    """
    左_str = str(左)
    右_str = str(右)
    # 初始占位：一个下划线 + 三个空格（足够输入0~100）
    初始占位 = "___"
    初始行 = f"{提示}【{左_str}_{初始占位}{右_str}】"
    sys.stdout.write(初始行)
    sys.stdout.flush()
    # 光标定位到第一个下划线处：提示长度 + '[' + 左数字长度 + 1（空格）
    光标偏移 = len(提示) + 1 + len(左_str) + 1
    sys.stdout.write('\r' + 初始行[:光标偏移])
    sys.stdout.flush()
    用户输入 = input()
    # # 清除当前行
    # sys.stdout.write('\r' + ' ' * len(初始行) + '\r')
    # # 输出最终格式化行
    # 最终行 = f"{提示}[{左_str} _{用户输入.strip()}_ {右_str}]"
    # sys.stdout.write(最终行 + '\n')
    # sys.stdout.flush()
    return 用户输入

