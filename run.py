# ======================= 核心算法 =======================
def 自由意志整数(起始: int, 终止: int, 自由意志: str = '') -> int:
    微秒时间 = int(str(round(time.time(), 6))[-3:])
    内存地址 = hash(id(time.time()))
    偏移量 = 起始
    区间长度 = 终止 - 起始
    自由意志 = hash(自由意志)
    if 自由意志:
        return round(abs(微秒时间 * 内存地址 * 自由意志) % (区间长度 - 1.2) + 0.6 + 偏移量)
    else:
        return round(abs(微秒时间 * 内存地址) % (区间长度 - 1.2) + 0.6 + 偏移量)

